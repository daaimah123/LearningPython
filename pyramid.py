# SET UP PYRAMID PROJECT FROM TUTORIAL
    # 1. pip install cookiecutter
    # 2. cookiecutter https://github.com/Pylons/pyramid-cookiecutter-starter
    # 3. cd <into_pyramid_project_name/>
    # 4. python3 -m venv <name of env>
    # 5. source <name of env>/bin/activate (deactivate to leave)
    # 6. pip install --upgrade pip setuptools
    # 7. pip install -e .
    # 8. pserve development.ini --reload
    # 9. click serving on http://localhost:6543 link to view

# MANUALLY SET UP PYRAMID PROJECT
    # 1. mkdir directory_name
    # 2. cd into dir
    # 3. python3 -m venv <name of env>
    # 4. source <name of env>/bin/activate (deactivate to leave)
    # 5. pip install pyramid